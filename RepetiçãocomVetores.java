import java.util.Scanner;

public class Repetição {

	public static void main(String[] args) {
		
		
		Scanner x = new Scanner(System.in);
		
		String [] alunos = new String [10];
		int[] idades = new int[10];
		int[] periodo = new int[10];
		
		
		for(int i=0; i<=5 ;i++) {
			
			System.out.print("digite os nomes");
			alunos[i] = x.nextLine();
			
			System.out.print("digite as idades");
			idades[i] = x.nextInt();
			
			System.out.print("digite os periodos");
			periodo[i] = x.nextInt();
			

			
		}
		
		
	}
}
		
		
		
		
		

	    
	
    	
    		
    	
    	
    	
    
    	
    	
	
	
			
	
	
	


